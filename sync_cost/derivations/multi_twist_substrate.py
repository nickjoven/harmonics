"""
Multi-twisted substrate: constructive realization in the framework vocabulary.

Conjecture: the framework's integer sectors -- (2, 3) from mass, 13
from half-Mobius, (13, 19) from cosmology, the Fibonacci backbone from
generations -- are projections of a single multi-twisted substrate.
This script attempts a constructive realization
in the framework's existing vocabulary (Stern-Brocot walks, tongue widths,
integer-labeled modes), without full AC or non-Hausdorff quotient spaces.

The construction has three pieces:

  1. BASE: the Stern-Brocot tree of rationals p/q, with denominators
     listed up to some cutoff q_max. This is the framework's existing
     discrete substrate; no new machinery.

  2. TWIST ALGEBRA: a finite-dimensional matrix algebra generated by two
     unitaries U, V satisfying U V = exp(2*pi*i*theta) V U, where theta
     is a rational approximation to 1/phi. This is a standard
     finite-dimensional analog of the irrational non-commutative torus,
     buildable in ZF without AC.

  3. PROJECTIONS: three filters (Z_2, Z_6, phi-rotation) applied to the
     base, each picking out a specific subset of denominators that match
     framework integers. The consistency check is that all three
     projections return their claimed integer sets on the SAME base.

If all three projections succeed on the same substrate without
contradiction, the multi-twisted substrate exists as a definite object
in the framework's vocabulary. We don't need the full infinite-
dimensional NCT; the finite Stern-Brocot truncation with a Z_n twist
algebra is sufficient to exhibit the three projections simultaneously.

What this closes: the "integer-level" version of item 14. The density
derivation of Omega_Lambda still lives in the Farey count ratio
(farey_partition.md), which is separate.

What this does not close: the absolute mass formula from the substrate.
The multi-twisted object gives the integer sectors but not the continuous
exponents a_1 per sector that the mass formula needs. That remains open
in item 12.

All constructions in this script are computable, deterministic, and
AC-free. No Zorn, no Hahn-Banach, no non-measurable sets.
"""

import math
from fractions import Fraction


PHI = (1 + math.sqrt(5)) / 2
INV_PHI = 1 / PHI


# ============================================================================
# Part 1: the base -- Stern-Brocot denominators up to q_max
# ============================================================================

def stern_brocot_rationals(q_max):
    """
    All irreducible fractions p/q with 0 <= p/q <= 1, q <= q_max.
    Returned in Farey order. This is the framework's existing substrate,
    truncated at depth q_max.
    """
    fracs = [Fraction(0, 1), Fraction(1, 1)]
    # insert mediants iteratively until no new fraction with q <= q_max
    while True:
        new_fracs = []
        for i in range(len(fracs) - 1):
            a, b = fracs[i], fracs[i+1]
            med = Fraction(a.numerator + b.numerator, a.denominator + b.denominator)
            if med.denominator <= q_max:
                new_fracs.append(med)
        if not new_fracs:
            break
        merged = sorted(set(fracs) | set(new_fracs))
        if len(merged) == len(fracs):
            break
        fracs = merged
    return fracs


def denominators(fracs):
    """Denominators of a list of fractions, sorted without duplicates."""
    return sorted(set(f.denominator for f in fracs))


# ============================================================================
# Part 2: the twist algebra -- finite-dimensional generators U, V
#
# Goal: realize the relation
#     U V = exp(2 pi i theta) V U
# as a concrete finite-dimensional unitary algebra. For rational theta = p/q
# (in lowest terms), the minimal faithful representation is q x q matrices.
# Choose p/q = F_{k-1}/F_k (Fibonacci convergent to 1/phi) so that theta
# approaches 1/phi as k grows.
#
# We don't actually need complex matrices for the consistency check; we
# only need the algebraic STRUCTURE. So we represent U and V as permutation-
# with-phase objects, and compute the commutator phase as a rational.
# ============================================================================

def twist_phase(p, q):
    """
    The phase of the twist algebra relation U V = exp(2 pi i p/q) V U.
    Represented as the rational p/q in lowest terms.
    """
    g = math.gcd(p, q)
    return Fraction(p // g, q // g)


def nct_finite(p, q):
    """
    Finite-dimensional non-commutative torus with theta = p/q.
    Returns a dict describing the algebra:
      dim: dimension of the minimal faithful representation
      theta: the twist parameter as a Fraction
      generators: symbolic names for U and V
      commutator: the rational phase factor in U V = e^(2 pi i theta) V U
      constructive: True (no AC used)
    """
    theta = twist_phase(p, q)
    return {
        "dim": theta.denominator,
        "theta": theta,
        "generators": ("U", "V"),
        "commutator": f"U V = exp(2 pi i * {theta}) V U",
        "constructive": True,
    }


def fibonacci_convergents_to_phi(max_k):
    """
    The Fibonacci convergents F_{k-1}/F_k -> 1/phi.
    Returns list of (p, q) = (F_{k-1}, F_k) for k = 2 to max_k.
    """
    F = [1, 1]
    while len(F) < max_k + 1:
        F.append(F[-1] + F[-2])
    return [(F[k-1], F[k]) for k in range(2, max_k + 1)]


# ============================================================================
# Part 3: the three projections
# ============================================================================

def project_z2(base):
    """
    Z_2 XOR projection on mode pairs: keep pairs (q_1, q_2) with
    q_1 + q_2 odd (one even, one odd). Returns the set of denominators
    that appear in any surviving pair. This is the Klein bottle's
    Z_2 filter.
    """
    qs = denominators(base)
    surviving_qs = set()
    for q1 in qs:
        for q2 in qs:
            if q1 != q2 and (q1 + q2) % 2 == 1:
                surviving_qs.add(q1)
                surviving_qs.add(q2)
    return sorted(surviving_qs)


def project_z6_residue(base):
    """
    Z_6 single-mode residue filter: keep denominators with q mod 6 == 1.
    First non-trivial survivors are 13 and 19 (see zn_twist_filter.py).
    """
    qs = denominators(base)
    return [q for q in qs if q % 6 == 1]


def project_z6_coprime(base):
    """
    Z_6 coprime filter: keep denominators coprime to 6.
    Gives {1, 5, 7, 11, 13, 17, 19, ...} at low q.
    """
    qs = denominators(base)
    return [q for q in qs if math.gcd(q, 6) == 1]


def project_phi_dirichlet(base):
    """
    Golden-ratio rotation filter: keep denominators q for which some
    numerator p satisfies the Dirichlet bound |p/q - 1/phi| < 1/(q^2 sqrt(5)).
    Returns denominators that are best approximants to 1/phi (Fibonacci
    denominators in the limit).
    """
    qs = denominators(base)
    survivors = []
    for q in qs:
        p = round(INV_PHI * q)
        if p == 0:
            p = 1
        err = abs(p / q - INV_PHI)
        bound = 1 / (q * q * math.sqrt(5))
        if err < bound:
            survivors.append(q)
    return survivors


