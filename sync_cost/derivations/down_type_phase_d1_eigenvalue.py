from fractions import Fraction


# ============================================================
# Lattice and group actions
# ============================================================

POINTS = [(x, y) for x in range(2) for y in range(3)]


def tau(p):
    x, y = p
    return ((1 - x) % 2, (-y) % 3)


def sigma(p):
    x, y = p
    return (x, (y + 1) % 3)


def orbit_under(start, generators):
    """Orbit of `start` under the subgroup generated by the callables
    in `generators`."""
    seen = {start}
    frontier = [start]
    while frontier:
        new_frontier = []
        for p in frontier:
            for g in generators:
                q = g(p)
                if q not in seen:
                    seen.add(q)
                    new_frontier.append(q)
        frontier = new_frontier
    return seen


def partition_into_orbits(points, generators):
    unseen = set(points)
    orbits = []
    while unseen:
        p = next(iter(unseen))
        orb = orbit_under(p, generators)
        orbits.append(frozenset(orb))
        unseen -= orb
    return orbits


